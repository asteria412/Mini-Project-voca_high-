import streamlit as st

def show_tutorial(expanded=False):
    # 호출되는 곳(컬럼)의 너비를 따라가므로, home.py에서 중앙 컬럼 안에 넣으면 예쁘게 나옵니다.
    with st.expander("📘 voca海 사용 가이드", expanded=expanded):
        st.markdown("""

## 0) 주의사항
- **새로고침**시 등록한 단어장 정보가 리셋됩니다.

### 1) 단어시험
- PDF/TXT 단어장을 업로드하시면, 단어를 무작위로 뽑아 시험을 냅니다.
- **중→한 / 한→중** 모드를 선택해 테스트할 수 있습니다.
- 정답 공개 시 **병음도 함께 보여줍니다** (채점은 한자/한글 기준).
- 여기서 올린 단어장은 어순 연습과 작문 문제에도 활용됩니다.

### 2) 어순 연습
- 단어장 기반으로 문장을 생성한 뒤, 어순을 섞어서 문제로 냅니다.
- 중국어 어순 감각을 반복 훈련하는 목적입니다.

### 3) 작문
- HSK 5급 유형처럼:
  - (A) 제시 단어로 80자 내외
  - (B) 그림 보고 80자 내외
- 작성하면 채점 + 피드백을 제공합니다.
- 100번 문제는 AI가 그림을 그려 출제합니다. 

### 4) 단어사전
- 먼저 **내 단어장 안에서 검색**합니다.
- 없으면 (추후) 검색/AI 도움으로 뜻 + 예문을 제공합니다.

### 5) 대시보드
- 항목별 점수와 학습 기록을 모아서 그래프로 보여줍니다.
- 사이드 바 상단 별명을 입력하셔야 점수저장 및 그래프 확인이 가능합니다.
- 별명을 기준으로 점수가 저장되어 동일한 별명 저장 시 지속 관리 가능합니다. 
        """)